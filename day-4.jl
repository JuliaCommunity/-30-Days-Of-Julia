# Example code:

# Define a matrix with 3 rows and 2 columns
A = [1 2; 3 4; 5 6]

# Access a specific element in the matrix
println(A[1, 2])

# Perform a matrix multiplication and print the result
B = [7 8; 9 10; 11 12]
